# using string
direction = "Right"

if(direction == "Right"): 
    print("going Right")
elif(direction == "Left"): 
    print("going Left")
else: 
    print("Unable to Navigate")


# using numbers
value = 0

if(value):
    print("non-zero will be True")
else:
    print("only 0 is False")

# using boolean
bool_val = True

if(bool_val):
    print("True is acceptable")
else:
    print("False is not acceptable")

# using list
a = [1]

if(a):
    print("non-empty list is True")
else:
    print("empty list will be False")